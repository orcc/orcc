package net.sf.orcc.ui.editors;

import org.eclipse.core.runtime.CoreException;
import org.eclipse.jface.text.IDocument;
import org.eclipse.jface.text.IDocumentPartitioner;
import org.eclipse.jface.text.rules.FastPartitioner;
import org.eclipse.ui.editors.text.FileDocumentProvider;

/**
 * This class defines a CAL document provider. It extends the file document
 * provider with the createDocument method that returns the partition scanner
 * for CAL documents.
 * 
 * @author Matthieu Wipliez
 * 
 */
public class CalDocumentProvider extends FileDocumentProvider {

	@Override
	protected IDocument createDocument(Object element) throws CoreException {
		IDocument document = super.createDocument(element);
		if (document != null) {
			IDocumentPartitioner partitioner = new FastPartitioner(
					new CalPartitionScanner(), new String[] {
							CalPartitionScanner.CAL_JAVADOC_COMMENT,
							CalPartitionScanner.CAL_MULTI_LINE_COMMENT,
							CalPartitionScanner.CAL_SINGLE_LINE_COMMENT });
			partitioner.connect(document);
			document.setDocumentPartitioner(partitioner);
		}
		return document;
	}

}
